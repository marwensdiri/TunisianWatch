    private void Update_table() {
        String requete = "select nom, prenom,mail  from Utilisateur";
        UtilisateurDao userDao = new UtilisateurDao();
        try {
            Statement statement = ResourceManager.getInstance()
                    .createStatement();
            ResultSet resultat = statement.executeQuery(requete);

            table_liste.setModel(DbUtils.resultSetToTableModel(resultat));

        } catch (SQLException ex) {
            //Logger.getLogger(PersonneDao.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("erreur lors du chargement des utilisateurs " + ex.getMessage());

        }
    }

